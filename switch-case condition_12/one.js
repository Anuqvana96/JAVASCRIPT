
//swith case
 
let grade = "A";
switch (grade){
    case "S":
        console.log("super grade");
        break;

    case "A":
        console.log("Excellent grade");
        break;

    case "E":
        console.log("Just pass");
        break;

    case "U":
        console.log("Failed grade");
        break;

    default:
        console.log("unknown grade");
}